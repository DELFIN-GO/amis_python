print('Програма визначає місце в строю хлопчика.\nВведіть спадну послідовність натуральних чисел, що не перевищує 200')
a = [int(a) for a in input().split()]
j = 0
x = int(input('Введіть зріст хлопчика\n'))
for i in range(0, len(a)):
    if a[i] >= x:
        j = j+1
print("Mісце хлопчика у строю під номером:",j + 1)
